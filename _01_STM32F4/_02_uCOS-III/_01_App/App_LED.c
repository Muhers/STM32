void LED(void *p_arg)	 
{     
	p_arg = p_arg; 
	
	GPIO_LED_Init();    
	
	while(1)
	{
		PF9 = !PF9;
		PF10 = !PF9;
		delay_ms(200);   
		
	}
	
}	

/* App 注册 -----------------------------*/  
